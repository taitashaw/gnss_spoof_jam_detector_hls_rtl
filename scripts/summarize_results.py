#!/usr/bin/env python3
"""summarize_results.py -- write results/summary.md from the actual metrics.

Produces a human-readable table over all scenarios with pass/fail, alert flags,
the headline scores, and latency. Pass/fail is recomputed with the same logic as
check_gnss_results.py so the summary never disagrees with the gate.

Author: John Bagshaw   License: MIT (c) 2026 John Bagshaw
"""
import json
import os

from gnss_cfg import load_config
from check_gnss_results import parse_windows, check_scenario, SCENARIOS

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
CFG = load_config()

FLAG_NAMES = [
    (CFG["FLAG_HIGH_POWER_JAM"], "power"),
    (CFG["FLAG_CN0_DROP"], "cn0"),
    (CFG["FLAG_CORR_ASYMMETRY"], "asym"),
    (CFG["FLAG_DOPPLER_ANOMALY"], "dopp"),
    (CFG["FLAG_SPOOF_SCORE_HIGH"], "spoof"),
    (CFG["FLAG_JAM_SCORE_HIGH"], "jam"),
    (CFG["FLAG_MALFORMED_PACKET"], "malformed"),
]


def flag_str(f):
    on = [nm for bit, nm in FLAG_NAMES if f & (1 << bit)]
    return ",".join(on) if on else "none"


def main():
    rows = []
    for s in SCENARIOS:
        apath = os.path.join(REPO, "results", s, "actual_metrics.txt")
        mpath = os.path.join(REPO, "vectors", s, "metadata.json")
        if not os.path.isfile(apath):
            rows.append((s, "MISSING", "-", "-", "-", "-", "-", "no results"))
            continue
        wins = parse_windows(apath)
        w = wins[-1] if wins else {}
        meta = json.load(open(mpath)) if os.path.isfile(mpath) else {}
        passed = check_scenario(s, verbose=False)
        rows.append((
            s,
            "PASS" if passed else "FAIL",
            flag_str(w.get("alert_flags", 0)),
            w.get("spoof_score", "-"),
            w.get("jam_score", "-"),
            w.get("cn0_proxy", "-"),
            w.get("symmetry_error", "-"),
            w.get("latency_cycles", "-"),
            meta.get("expected_alert_class", "-"),
        ))

    out = os.path.join(REPO, "results", "summary.md")
    with open(out, "w") as f:
        f.write("# GNSS Spoof/Jam Detector -- Results Summary\n\n")
        f.write("Generated by `scripts/summarize_results.py`. "
                "Source of metric values: the C golden model "
                "(`make selfcheck`) or XSim (`make xsim`).\n\n")
        f.write("| Scenario | Result | Alert Flags | spoof | jam | cn0 | symmetry | latency | Class |\n")
        f.write("|---|---|---|---|---|---|---|---|---|\n")
        for r in rows:
            f.write("| " + " | ".join(str(x) for x in r) + " |\n")
        npass = sum(1 for r in rows if r[1] == "PASS")
        f.write(f"\n**{npass}/{len(rows)} scenarios passed.**\n")
        f.write("\nNotes:\n")
        f.write("- Alert flags are the exact assertion; scores/cn0/symmetry are "
                "checked against loose ranges.\n")
        f.write("- `latency` is the metric-engine latency in cycles "
                "(software-model estimate for `make selfcheck`; cycle-accurate "
                "from `axis_latency_counter` for `make xsim`).\n")
    print(f"Wrote {out} ({npass}/{len(rows)} passed)")


if __name__ == "__main__":
    main()
